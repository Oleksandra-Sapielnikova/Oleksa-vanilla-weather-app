function formatDate(timestamp) {
  //calculate the date from gived dt miliseconds from 1970
  let date = new Date(timestamp);
  let hours = date.getHours();
  if (hours < 10) {
    hours = `0${hours}`;
  }
  let minutes = date.getMinutes();
  if (minutes < 10) {
    minutes = `0${minutes}`;
  }
  let days = [
    "Sunday",
    "Monday",
    "Tuesday",
    "Wednesday",
    "Thursday",
    "Friday",
    "Saurday",
  ];
  let day = days[date.getDay()];
  return `${day} ${hours}:${minutes}`;
}

